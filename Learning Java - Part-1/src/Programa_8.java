//Using switch

public class Programa_8 {
	
	public static void main(String [] args){
		
		int numero=1;
		
		
		switch(numero){
			
		case 1:
			System.out.println("Caiu no case 1");
			break;
			
		case 2:
			System.out.println("Caiu no case 2");
			break;
			
		default:
			System.out.println("Nenhuma das opções");
					
		
			
		}//switch		
		
	}//main
}//class
