//Using for
//Using while

public class Programa_6 {
	
	public static void main (String[] args){
		
		
		for (int i=0; i<10; i++){
			System.out.println("Using for: " +i);
		}
		
		
		
		int j = 0;
		while(j <= 10){
			System.out.println("Using while: " +j);
			j++;			
		}
		
		
		
		
		
	}

}
